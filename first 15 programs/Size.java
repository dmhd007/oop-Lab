class Size{
        public static void main(String args[]){
            int size=42;
            switch(size){
                case 29 : System.out.println("size is small");
                        break;
                case 42 : System.out.println("size is medium");
                        break;       
                case 44 : System.out.println("size is large");
                        break; 
                case 48 : System.out.println("size is Extra large");
                        break;  
                default : System.out.println("size unknown");
                                              
            }
        }

}